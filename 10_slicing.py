nombre = 'Luisa Guavita'
# Indexing: Posición índice letra L
print(nombre [4])
#slicing = Valor indice inicial normal y el v alor final se suma uno más
print(nombre[1:4])
print(nombre[7:14])
print(nombre[0:14])
print(nombre[0:]) #imprmir toda la variable
# Para imprimir letras intercaladas se ubican print(nombrevariable::[caracter inicial; caracter final; numero intercalado de saltos]):
print(nombre[0:14:2])
print(nombre[0::2])
# Regresivo
print('regresivo')
print(nombre[-1:-9])
print(nombre[-1:-9:-1])